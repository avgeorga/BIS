CREATE TABLE PRODUCTS (
    STOCKCODE STRING,
    DESCRIPTION STRING,
    UNITPRICE FLOAT
)
PARTITIONED BY (LOADDATE INT) -- LOADDATE must be YYYYMMDD
STORED AS PARQUET
LOCATION 'data/out/products';

CREATE TABLE CUSTOMERS (
    CUSTOMERID STRING,
    COUNTRY STRING
)
PARTITIONED BY (LOADDATE INT) -- LOADDATE must be YYYYMMDD
STORED AS PARQUET 
LOCATION '/data/out/customers'; 

CREATE TABLE ORDERS (
    STOCKCODE STRING,
    QUANTITY INT,
    INVOICEDATE STRING,
    CUSTOMERID STRING
)
PARTITIONED BY (LOADDATE INT) -- LOADDATE must be YYYYMMDD
STORED AS PARQUET 
LOCATION '/data/out/orders'; 